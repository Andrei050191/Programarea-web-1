// definim constanta
const PI = 3.14159;

// definim variabila raza
let radius = 5;

// calcul aria cercului
let area = PI * radius * radius;

// calcul circumferința cercului
let circumference = 2 * PI * radius;

// afișare rezultate
console.log("Raza:", radius);
console.log("Aria cercului:", area);
console.log("Circumferința cercului:", circumference);

